#ifndef _TIMER_H
#define _TIMER_H

struct timeval;
struct timer;

typedef void timer_cb(void *data);

struct timer *tmr_creat(const struct timeval *delay,
			const struct timeval *repeat,
			timer_cb *cb, void *data, int start);
void tmr_free(struct timer *tmr);

void tmr_reset(struct timer *tmr,
	       const struct timeval *delay,
	       const struct timeval *repeat);
void tmr_start(struct timer *tmr);
void tmr_stop(struct timer *tmr);

#endif
