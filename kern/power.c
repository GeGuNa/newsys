#include <kern/power.h>
#include <kern/hw.h>

static struct power_ops *pwr_ops;

void pwr_install(struct power_ops *pwo)
{
	pwr_ops = pwo;
}

void power_down(void)
{
	if (pwr_ops != NULL && pwr_ops->power_down != NULL)
		pwr_ops->power_down();
}

void halt(void)
{
	if (pwr_ops != NULL && pwr_ops->halt != NULL)
		pwr_ops->halt();
}
