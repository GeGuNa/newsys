#include <sys/wait.h>
#include <stdlib.h>
#include <signal.h>
#include <newtask.h>

int system(const char *comm)
{
	int status;
	pid_t xpid;
	pid_t pid;
	
	pid = _newtaskl("/bin/sh", "/bin/sh", "-c", comm, (void *)NULL);
	if (pid < 0)
		return -1;
	
	while (xpid = wait(&status), xpid > 0)
		if (xpid == pid)
			return status;
	
	return -1;
}
